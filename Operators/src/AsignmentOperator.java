
public class AsignmentOperator {
	public static void main(String args[]) {
	      int num1 = 50;
	      int num2 = 25;

	      num2 = num1;
	      System.out.println("= Output: "+num2);

	      num2 += num1;
	      System.out.println("+= Output: "+num2);
		      
	      num2 -= num1;
	      System.out.println("-= Output: "+num2);
		      
	      num2 *= num1;
	      System.out.println("*= Output: "+num2);
		      
	      num2 /= num1;
	      System.out.println("/= Output: "+num2);
		      
	      num2 %= num1;
	      System.out.println("%= Output: "+num2);
	   }

}
